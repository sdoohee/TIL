# 리터럴 : 변수에 저장되는 값 자체

# 문자열 리터럴
str1 = "홍길동"
str2 = "Python"
str3 = """Python Programming"""
str4 = '''파이썬 프로그래밍'''
str5 = """여러줄로
          나누어
          출력해도 됨"""

print(str1)
print(str2)
print(str3)
print(str4)
print(str5)

# 논리값 리터럴 (Boolean Literals)
t = True
f = False
print(t, f)
print(type(t))

# 특수 리터럴
name = "홍길동"
phone = '' # 공백
address = None
print(name, phone, address)
print(type(phone))
print(type(address))

